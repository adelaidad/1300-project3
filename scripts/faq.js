// when #Q1 is clicked:
$("#Q1").click(function() {
    if ($("#A1").hasClass("hidden")) {
      $("#A1").removeClass("hidden");
      $("#plus1").addClass("hidden");
      $("#minus1").removeClass("hidden");
    } else {
      $("#A1").addClass("hidden");
      $("#plus1").removeClass("hidden");
      $("#minus1").addClass("hidden");
    }
  });


